// Function to search by district (division) with partial match
function searchByDistrict(district) {
  return Object.fromEntries(
    Object.entries(data).filter(([key]) =>
      key.toLowerCase().includes(district.toLowerCase())
    )
  )
}

// Function to search by city with partial match
function searchByCity(city) {
  if (typeof city !== "string")
    return "Error: searchByCity - Please input a valid city name"

  let results = []
  for (const district in data) {
    results.push(
      ...data[district].filter((entry) =>
        entry.city.toLowerCase().includes(city.toLowerCase())
      )
    )
  }

  if (results.length === 1) return results[0] // Return a single object if only one match

  return results // Return an array if multiple matches found
}

// Function to search by sub with partial match
function searchBySub(sub) {
  if (typeof sub !== "string")
    return "Error: searchBySub - Please input a valid Name"

  let results = []
  for (const district in data) {
    results.push(
      ...data[district].filter((entry) =>
        entry.sub.toLowerCase().includes(sub.toLowerCase())
      )
    )
  }
  if (results.length == 1) return results[0]

  return results
}

// Function to search by postal code with partial match
function searchByCode(zipcode) {
  zipcode = String(zipcode)
  let results = []
  for (const district in data) {
    results.push(
      ...data[district].filter((entry) => entry.postalCode.includes(zipcode))
    )
  }
  if (results.length == 1) return results[0]

  return results
}

